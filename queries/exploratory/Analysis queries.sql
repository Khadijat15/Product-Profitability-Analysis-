--- Question: What is the total revenue made per store?
/* Answer*/

SELECT S.StoreID, S.StoreName, SUM(P.UnitPrice*T.Quantity*(1-T.Discount)) AS TotalSales
FROM store S
JOIN transaction T
ON S.StoreID = T.StoreID
JOIN product P
ON P.ProductID = T.ProductID
GROUP BY S.StoreID,S.StoreName
ORDER BY TotalSales DESC;


--- Question: What is the total profit made per store?
/* Answer*/

SELECT S.StoreID, S.StoreName,SUM(P.UnitPrice*T.Quantity*(1-T.Discount) -(P.CostPrice*T.Quantity))
AS TotalProfit
FROM store S
JOIN transaction T
ON S.StoreID = T.StoreID
JOIN product P
ON P.ProductID = T.ProductID
GROUP BY S.StoreID,S.StoreName
ORDER BY TotalProfit DESC;


--- Question: What is the total profit made per products?
/* Answer*/

SELECT P.ProductID,P.ProductName,SUM(P.UnitPrice*T.Quantity*(1-T.Discount) -(P.CostPrice*T.Quantity))
AS TotalProfit
